// Estrutura de repetição
// for



// break; => para a execução do loop
// continue; => pula a execução do momento
for(let i = 10; i > 0; i--){
    

    if(i === 5){
        continue; // "continue;" pula o resto dos comandos do loop e retorna no começo, dando seguimento normal ao resto do loop, caso ainda tenho repetições a fazer;
    }
    console.log(i)
}